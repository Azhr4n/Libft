#include <stdlib.h>
#include <stdio.h>
#include "libft.h"

void	ft_perror(char *name, char *error, int iexit)
{
	if (name)
	{
		ft_putstr_fd(name, FT_STDERR);
		ft_putstr_fd(": ", FT_STDERR);
	}
	perror(error);
	if (iexit)
		exit(iexit);
}
