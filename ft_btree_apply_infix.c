#include "libft.h"

void	ft_btree_apply_infix(t_btree *root, void (*applyf)(t_ftbuffer *))
{
	if (root && applyf)
	{
		if (root->left)
			ft_btree_apply_infix(root->left, applyf);
		(*applyf)(root->data);
		if (root->right)
			ft_btree_apply_infix(root->right, applyf);
	}
}
