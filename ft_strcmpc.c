#include "libft.h"

int		ft_strcmpc(char *s1, char *s2, char c)
{
	while (*s1++ == *s2++ && (*s1 || *s2) && *s1 != c)
		;
	return ((int)(*(t_uchar*)(--s1) - *(t_uchar*)(--s2)));
}
