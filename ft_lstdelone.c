#include <stdlib.h>

#include "libft.h"

void	ft_lstdelone(t_lst **alst, void (*del)(void *, size_t))
{
	if (alst && *alst)
	{
		if (del)
			(*del)((*alst)->data, (*alst)->size);
		free((*alst)->data);
		free(*alst);
		*alst = NULL;
	}
}
